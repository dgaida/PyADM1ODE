# benchmark/eval/make_index.py
"""
Erzeugt ``dataset/index.json`` und aktualisiert den embedded-Block in
``viewer/index.html`` mit den aktuellen Datenpunkten. Nach dem
Hinzufuegen/Aendern von Datenpunkten erneut ausfuehren:

    python benchmark/eval/make_index.py

Reines stdlib (laeuft ueberall). Listet jede *.json unter dataset/, die ein
Datenpunkt ist (hat ``reference`` + ``input``), mit Pfad relativ zu dataset/.
Der embedded-Block in viewer/index.html wird zwischen den Markern

    // @@BEGIN_EMBEDDED@@
    // @@END_EMBEDDED@@

ersetzt. Alle anderen Teile der HTML-Datei bleiben unveraendert.
"""

import glob
import json
import os
import re

HERE = os.path.dirname(os.path.abspath(__file__))
DATASET = os.path.abspath(os.path.join(HERE, "..", "dataset"))
VIEWER_HTML = os.path.abspath(os.path.join(HERE, "..", "viewer", "index.html"))

BEGIN_MARKER = "// @@BEGIN_EMBEDDED@@"
END_MARKER = "// @@END_EMBEDDED@@"


def collect_datapoints() -> list:
    entries = []
    for path in sorted(glob.glob(os.path.join(DATASET, "**", "*.json"), recursive=True)):
        if os.path.basename(path) == "index.json":
            continue
        try:
            d = json.load(open(path, encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue
        if not (isinstance(d, dict) and "reference" in d and "input" in d):
            continue
        entries.append((path, d))
    return entries


def write_index_json(entries: list) -> None:
    records = []
    for path, d in entries:
        rel = os.path.relpath(path, DATASET).replace(os.sep, "/")
        inp = d.get("input", {})
        records.append(
            {
                "id": d.get("id", rel),
                "path": rel,
                "language": inp.get("language"),
                "modality": inp.get("modality"),
                "regime": d.get("regime"),
            }
        )
    out = os.path.join(DATASET, "index.json")
    with open(out, "w", encoding="utf-8") as fh:
        json.dump({"datapoints": records}, fh, ensure_ascii=False, indent=2)
    print(f"{len(records)} Datenpunkte -> {os.path.relpath(out, os.path.join(HERE, '..', '..'))}")


def update_viewer_embedded(entries: list) -> None:
    if not os.path.exists(VIEWER_HTML):
        print(f"Viewer nicht gefunden: {VIEWER_HTML} — uebersprungen")
        return

    with open(VIEWER_HTML, encoding="utf-8") as fh:
        html = fh.read()

    if BEGIN_MARKER not in html or END_MARKER not in html:
        print(f"Marker nicht gefunden in {os.path.basename(VIEWER_HTML)} — embedded-Block nicht aktualisiert")
        return

    datapoints = [d for _, d in entries]
    dp_json = json.dumps(datapoints, ensure_ascii=False, indent=2)

    new_block = (
        f"{BEGIN_MARKER}\n"
        f"// Auto-generated by benchmark/eval/make_index.py — do not edit manually.\n"
        f"const EMBEDDED = {dp_json};\n"
        f"{END_MARKER}"
    )

    pattern = re.compile(
        re.escape(BEGIN_MARKER) + r".*?" + re.escape(END_MARKER),
        re.DOTALL,
    )
    # Use a lambda so re.sub does not process \n in new_block as escape sequences.
    updated, n = pattern.subn(lambda _: new_block, html)
    if n == 0:
        print("Ersetzen fehlgeschlagen — embedded-Block unveraendert")
        return

    with open(VIEWER_HTML, "w", encoding="utf-8") as fh:
        fh.write(updated)
    print(f"viewer/index.html embedded-Block aktualisiert ({len(datapoints)} Datenpunkte)")


def main() -> None:
    entries = collect_datapoints()
    write_index_json(entries)
    update_viewer_embedded(entries)


if __name__ == "__main__":
    main()
